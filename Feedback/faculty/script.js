window.onload = function() {
  var sel1 = document.getElementById("sel1");
  var sel2 = document.getElementById("sel2");
  var sel3 = document.getElementById("sel3");

  for (var x in Object1) {
    sel1.options[sel1.options.length] = new Option(x, x);
  }
  sel1.onchange = function() {
    //empty Chapters- and Topics- dropdowns
    sel3.length = 1;
    sel2.length = 1;
    //display correct values
    for (var y in Object1[this.value]) {
      sel2.options[sel2.options.length] = new Option(y, y);
    }
  }
  sel2.onchange = function() {
    //empty Chapters dropdown
    sel3.length = 1;
    //display correct values
    var z = Object1[sel1.value][this.value];
    for (var i = 0; i < z.length; i++) {
      sel3.options[sel3.options.length] = new Option(z[i], z[i]);
    }
  }
}

